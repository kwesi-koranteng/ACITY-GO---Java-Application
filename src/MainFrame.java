    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
    String idText = tfId.getText();
if (idText.isEmpty()) {
    JOptionPane.showMessageDialog(null, "ID field must be filled!", "Error", JOptionPane.ERROR_MESSAGE);
    return;
}

int id;
try {
    id = Integer.parseInt(idText);
} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(null, "ID must be a number!", "Error", JOptionPane.ERROR_MESSAGE);
    return;
}

String name = tfName.getText();
String email = tfEmail.getText();
String phone = tfPhone.getText();
String location = tfLocation.getText();
String destination = tfDestination.getText();

// Check if any field is empty
if (name.isEmpty() || email.isEmpty() || phone.isEmpty() || location.isEmpty() || destination.isEmpty()) {
    JOptionPane.showMessageDialog(null, "All fields must be filled!", "Error", JOptionPane.ERROR_MESSAGE);
    return;
}

// Insert into database
try {
    String sql = "INSERT INTO clients (tfId, tfName, tfEmail, tfPhone, tfLocation, tfDestination) VALUES (?, ?, ?, ?, ?, ?)";
    PreparedStatement stmt = dbConnection.prepareStatement(sql);
    stmt.setInt(1, id);
    stmt.setString(2, name);
    stmt.setString(3, email);
    stmt.setString(4, phone);
    stmt.setString(5, location);
    stmt.setString(6, destination);
    
    int rowsInserted = stmt.executeUpdate();
    if (rowsInserted > 0) {
        JOptionPane.showMessageDialog(null, "Client added successfully!");
    } else {
        JOptionPane.showMessageDialog(null, "Failed to add client!", "Error", JOptionPane.ERROR_MESSAGE);
    }
    
    stmt.close();
} catch (SQLException ex) {
    ex.printStackTrace();
    JOptionPane.showMessageDialog(null, "Database error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
}



    }//GEN-LAST:event_btnAddActionPerformed

