    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        int selectedRow = tableclients.getSelectedRow(); // Get selected row index

    if (selectedRow != -1) { // Ensure a row is selected
        DefaultTableModel model = (DefaultTableModel) tableclients.getModel();

        // Get new values from text fields
        int id = Integer.parseInt(tfId.getText());
        String name = tfName.getText();
        String email = tfEmail.getText();
        String phone = tfPhone.getText();
        String location = tfLocation.getText();
        String destination = tfDestination.getText();

        // Update the selected row
        model.setValueAt(id, selectedRow, 1);
        model.setValueAt(name, selectedRow, 2);
        model.setValueAt(email, selectedRow, 3);
        model.setValueAt(phone, selectedRow, 4);
        model.setValueAt(location, selectedRow, 5);
        model.setValueAt(destination, selectedRow, 6);
        
        JOptionPane.showMessageDialog(this, "Entry updated successfully!");
    } else {
        JOptionPane.showMessageDialog(this, "Please select a row to update.");
    }
    }//GEN-LAST:event_updateButtonActionPerformed

